return describe("worker_reject RTP helpers", function()
  local worker_reject = require("worker_reject")
  describe("ip classification", function()
    it("identifies private and public IPv4", function()
      assert.is_true(worker_reject.is_private_ipv4("10.35.3.6"))
      assert.is_true(worker_reject.is_private_ipv4("192.168.1.2"))
      assert.is_false(worker_reject.is_private_ipv4("83.136.163.31"))
      assert.is_true(worker_reject.is_public_ipv4("83.136.163.31"))
      return assert.is_false(worker_reject.is_public_ipv4("10.35.3.6"))
    end)
    it("plage 172.16.0.0/12 est privée", function()
      assert.is_true(worker_reject.is_private_ipv4("172.16.0.1"))
      assert.is_true(worker_reject.is_private_ipv4("172.31.255.255"))
      assert.is_false(worker_reject.is_private_ipv4("172.15.0.1"))
      return assert.is_false(worker_reject.is_private_ipv4("172.32.0.1"))
    end)
    it("loopback et link-local ne sont pas publics", function()
      assert.is_false(worker_reject.is_public_ipv4("127.0.0.1"))
      return assert.is_false(worker_reject.is_public_ipv4("169.254.1.1"))
    end)
    return it("nil et format invalide → false", function()
      assert.is_false(worker_reject.is_private_ipv4(nil))
      assert.is_false(worker_reject.is_private_ipv4("not_an_ip"))
      assert.is_false(worker_reject.is_public_ipv4(nil))
      return assert.is_false(worker_reject.is_public_ipv4(""))
    end)
  end)
  describe("RTP payload detector", function()
    it("accepts RTP v2-like UDP payload", function()
      local udp_header = string.rep("\0", 8)
      local rtp_payload = string.char(0x80, 0x60, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, 0x12, 0x34, 0x56, 0x78)
      local raw = udp_header .. rtp_payload
      return assert.is_true(worker_reject.looks_like_rtp_payload(raw, 1))
    end)
    it("rejects STUN magic-cookie payload", function()
      local udp_header = string.rep("\0", 8)
      local stun_like = string.char(0x80, 0x01, 0x00, 0x00, 0x21, 0x12, 0xA4, 0x42, 0x00, 0x00, 0x00, 0x00)
      local raw = udp_header .. stun_like
      return assert.is_false(worker_reject.looks_like_rtp_payload(raw, 1))
    end)
    it("payload trop court → false", function()
      local raw = string.rep("\0", 10)
      return assert.is_false(worker_reject.looks_like_rtp_payload(raw, 1))
    end)
    return it("version bits ≠ 2 → false", function()
      local udp_header = string.rep("\0", 8)
      local bad_rtp = string.char(0x40, 0x60, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, 0x12, 0x34, 0x56, 0x78)
      local raw = udp_header .. bad_rtp
      return assert.is_false(worker_reject.looks_like_rtp_payload(raw, 1))
    end)
  end)
  return describe("RTP tuple tracker", function()
    it("tracks private->public high-port RTP-like UDP only", function()
      local udp_header = string.rep("\0", 8)
      local rtp_payload = string.char(0x80, 0x60, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, 0x12, 0x34, 0x56, 0x78)
      local raw = udp_header .. rtp_payload
      local sip_ports = {
        [5060] = true
      }
      assert.is_true(worker_reject.should_track_rtp_udp(17, 4, "10.35.3.6", "83.136.163.31", 16440, 57044, raw, 1))
      assert.is_false(worker_reject.should_track_rtp_udp(17, 4, "10.35.3.6", "10.35.3.7", 16440, 57044, raw, 1))
      return assert.is_false(worker_reject.should_track_rtp_udp(17, 4, "10.35.3.6", "83.136.163.31", 5060, 57044, raw, 1, sip_ports))
    end)
    it("rejet si proto != UDP (TCP)", function()
      local udp_header = string.rep("\0", 8)
      local rtp_payload = string.char(0x80, 0x60, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, 0x12, 0x34, 0x56, 0x78)
      local raw = udp_header .. rtp_payload
      return assert.is_false(worker_reject.should_track_rtp_udp(6, 4, "10.35.3.6", "83.136.163.31", 16440, 57044, raw, 1))
    end)
    it("rejet si version IPv6", function()
      local udp_header = string.rep("\0", 8)
      local rtp_payload = string.char(0x80, 0x60, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, 0x12, 0x34, 0x56, 0x78)
      local raw = udp_header .. rtp_payload
      return assert.is_false(worker_reject.should_track_rtp_udp(17, 6, "10.35.3.6", "83.136.163.31", 16440, 57044, raw, 1))
    end)
    it("rejet si sport < 1024", function()
      local udp_header = string.rep("\0", 8)
      local rtp_payload = string.char(0x80, 0x60, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, 0x12, 0x34, 0x56, 0x78)
      local raw = udp_header .. rtp_payload
      return assert.is_false(worker_reject.should_track_rtp_udp(17, 4, "10.35.3.6", "83.136.163.31", 53, 57044, raw, 1))
    end)
    return it("rejet si port exclu", function()
      local udp_header = string.rep("\0", 8)
      local rtp_payload = string.char(0x80, 0x60, 0x00, 0x01, 0x00, 0x00, 0x00, 0x01, 0x12, 0x34, 0x56, 0x78)
      local raw = udp_header .. rtp_payload
      local excluded = {
        [16440] = true
      }
      return assert.is_false(worker_reject.should_track_rtp_udp(17, 4, "10.35.3.6", "83.136.163.31", 16440, 57044, raw, 1, excluded))
    end)
  end)
end)
